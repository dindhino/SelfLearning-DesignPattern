public class Student
{
    private String rollNo;
    private String name;

    public String GetRollNo()
    {
        return rollNo;
    }

    public void SetRollNo(String rollNo)
    {
        this.rollNo = rollNo;
    }

    public String GetName()
    {
        return name;
    }

    public void SetName(String name)
    {
        this.name = name;
    }
}